        </p>
    <h3>4.1 Cookies o Galletas Informáticas</h3>
    <p>Una cookie es un pequeño fichero de texto que se almacena en su navegador cuando visita 
        casi cualquier página web. Su utilidad es que la web sea capaz de recordar su visita cuando 
        vuelva a navegar por esa página.</p>
    <p>Las cookies suelen almacenar información de carácter técnico, preferencias personales, 
        personalización de contenidos, estadísticas de uso, enlaces a redes sociales, acceso a cuentas 
        de USUARIO, etc. El objetivo de la cookie es adaptar el contenido de la web a su perfil y 
        necesidades, sin cookies los servicios ofrecidos por cualquier página se verían mermados 
        notablemente.</p>
    <p>Nuestras cookies se utilizan para analizar su forma de interactuar con nuestro SITIO WEB y 
        nuestra APP, con el fin de optimizar nuestros SERVICIOS y para mejorar su experiencia. Por 
        la manera en que funciona el SITIO WEB y la APP, si el USUARIO desactiva o bloquea la 
        utilización de este sistema, se pueden ocasionar errores en el funcionamiento y/o la 
        utilización del SITIO WEB y de la APP.</p>
    <h3>4.2 Web beacons o Faro Web</h3>
    <p>Un “web beacon” o faro web es un archivo de imagen utilizado para seguir la pista a la 
        navegación del USUARIO por medio del Sitio web. Los web beacons, también llamados “web 
        bugs” son utilizados fundamentalmente para dar seguimiento a la navegación del USUARIO 
        por el SITIO WEB y/o la APP. Por la manera en que funciona el Sitio Web, si el USUARIO 
        desactiva o bloquea la utilización de este sistema, se pueden ocasionar errores en el 
        funcionamiento y/o la utilización del SITIO WEB y/o la APP.
        </p>
    <h3>4.3 Web server logs</h3>
    <p>Un web server log es un archivo simple de texto que graba la actividad en un servidor. Es te 
        tipo de sistema permite obtener estadísticas sobre los patrones de navegación de los 
        Usuarios. Por la manera en que funciona el Sitio Web y la App, si el USUARIO desactiva o 
        bloquea la utilización de este sistema, se pueden ocasionar errores en el funcionamiento y/o 
        la utilización del SITIO WEB y la APP</p>
    <h3>4.4 Tecnología de Geolocalización</h3>
    <p>La tecnología de geolocalización permite la determinación de la ubicación del USUARIO por 
        medio de las coordinadas geográficas (latitud y longitud) en un mapa común. La información 
        será obtenida por medio de la identificación del IP de un aparato conectado a internet, por 
        medio de un proveedor de red, por medio de un aparato con red GSM o por medio de un GPS, 
        por lo que serán aplicables, a tales servicios de geolocalización, los términos y condiciones 
        del tercero proveedor. Por la manera en que funciona el SITIO WEB y la APP, si el USUARIO 
        desactiva o bloquea la utilización de este sistema, se pueden ocasionar errores en el 
        funcionamiento y/o la utilización del SITIO WEB y la APP.</p>
    <p>Para evitar que su aparato permita el uso de las tecnologías antes dichas, el USUARIO debe 
        rechazar la solicitud de autorización que se desplegará en la pantalla de su aparato, previo a 
        la navegación por determinadas secciones de nuestro SITIO WEB y la APP.
        </p>
    <p>	Si el USUARIO tiene alguna duda sobre las tecnologías antes dichas, cómo funcionan, cómo 
        desactivarlas o cualquier otro tipo de inquietud en relación con las que se utilizan en nuestro 
        SITIO WEB y la APP, deberá comunicarse con nosotros por medio de las líneas telefónicas 
