    <h4>Los servicios.</h4><br>
    <p>El Operador exhibe a través de La Plataforma productos de consumo como alimentos, bebidas, productos de aseo general y productos en general, que están a disposición de los Consumidores para su conocimiento general. Esta comunicación de productos sirve como referencia a los Consumidores para poder usar La Plataforma como medio para solicitar la gestión de un encargo de compraventa, celebrándose un contrato de mandato remunerado con el Comisionista.</p>
    <p>A no ser que NeniEntregas lo acepte mediante un contrato separado por escrito con los Consumidores, los Servicios se ponen a disposición solo para su uso personal, no comercial. Cualquier Consumidor que acceda y/o use la Plataforma, reconoce que los Servicios de NeniEntregas no incluyen servicios de venta de productos, de entrega a domicilio o de logística, en cuanto la relación contractual se constituye directamente con entre el Consumidor y el Comisionista, quien actúa de manera independiente y bajo ninguna circunstancia puede ser entendido como un empleado de NeniEntregas o de sus afiliadas.</p>
    <h4>Objeto de los servicios.</h4><br>
    <p>El Consumidor reconoce y acepta usar la Plataforma como un medio de encuentro con Comisionistas para la solicitud de encargos y de esta manera lograr la adquisición de Productos para su consumo final y la satisfacción de sus necesidades, con sujeción a la normatividad mercantil mexicana.</p>
    <p>El uso de la Plataforma lo realiza el Consumidor como persona capaz y mayor de edad, y manifestando que para la celebración del contrato de Mandato con los Comisionistas, cuenta con plena capacidad legal para ser sujeto de derechos y obligaciones, calidades que refrenda al momento de generar su registro.</p>
    <p>El Consumidor reconoce expresamente mediante la aceptación de los presentes Términos y Condiciones de uso de la Plataforma que NeniEntregas no será responsable en ningún momento de cualquier producto que los Consumidores requieran y adquieran a través de la Plataforma. Asimismo, NeniEntregas tampoco podrá observar y verificar el correcto funcionamiento de la Plataforma en relación cualquier otro producto distinto a aquellos exhibidos en la Plataforma.</p>
    <p>Los Comisionistas, teniendo capacidad de contratación, acceden a la Plataforma y aceptan realizar la gestión de los encargos solicitados por los Consumidores, entendiéndose que dicha aceptación es meramente voluntaria.</p>
    <p>El perfeccionamiento del contrato de mandato celebrado por medios electrónicos se da en el momento en que el Comisionista acepta la gestión del encargo solicitado por el Consumidor. Se aclara que el Comisionista únicamente puede aceptar el encargo, una vez el Consumidor haya seleccionado el medio de pago. Por lo tanto, verificados los pasos de proceso de compra, el Comisionista, por intermedio del Operador puede aceptar total o parcialmente la gestión del encargo solicitado por el Consumidor. En caso de aceptar el encargo únicamente de manera parcial, el resumen se actualizará evidenciando los Productos sobre los cuales efectivamente se entiende constituido el encargo.</p>

