    <strong><h1>Términos & Condiciones de Uso</h1></strong>

    <p>Los presentes Términos y Condiciones regulan la autorización de uso que otorga el Operador a los Consumidores, para que éstos ingresen a la Plataforma virtual, se informen sobre los Productos de consumo exhibidos  para que sean utilizados como referencia y puedan solicitar la gestión de un encargo por medio de un contrato de mandato con el Comisionista.

        El Operador, a través de la Plataforma realiza, de manera limitativa, las siguientes acciones: (1) exhibe diferentes productos de consumo de forma publicitaria para que puedan servir de referencia a los Consumidores; (2) facilita el encuentro entre Consumidores y Comisionistas para la realización del vínculo contractual; (3) permite el uso de la Pasarela de Pagos, sin que en este proceso implique vínculo alguno con el Operador, ya que por la tecnología implementada el pago se realiza directamente a los Comisionistas; (4) sirve de medio de envío de comunicaciones entre los Consumidores y los Comisionistas.
        
        La celebración de la relación contractual entre Consumidores y Comisionista, se da con Consumidores que se encuentren en el territorio nacional o que, estando en el extranjero, solicitan la gestión de un encargo, que debe ser realizado en territorio mexicano, pagando una contraprestación económica mediante el sistema de pago electrónico a través de la Pasarela de Pago contratada por el Operador o en dinero en efectivo al momento de la recepción de los Productos, a elección del Consumidor.
        
        A través de la Plataforma se exhiben Productos, cuya gestión de compra es encargada por los Consumidores a los Comisionistas, siendo que los primeros buscan satisfacer una necesidad privada, personal o familiar, y en ningún momento pretenden la comercialización, reventa o cualquier otro tipo de transacción comercial o interés con los productos adquiridos.
        </p>
    
